def format_report(analysis: CapacityAnalysis, phases: list[PhaseResult]) -> str:
    """Format the capacity analysis as a human-readable report."""
    lines = []
    w = lines.append

    w("=" * 78)
    w("  FITSI IA — CAPACITY ANALYSIS REPORT")
    w("=" * 78)
    w("")

    # Executive Summary
    w("1. EXECUTIVE SUMMARY")
    w("-" * 78)
    w(f"  Max stable users:      {analysis.max_stable_users:,} ({analysis.max_stable_phase})")
    w(f"  Degradation starts at: {analysis.degradation_start_users:,} ({analysis.degradation_start_phase})")
    w(f"  Breaking point:        {analysis.breaking_point_users:,} ({analysis.breaking_point_phase})")
    w(f"  Max sustained RPS:     {analysis.max_sustained_rps:,.1f} req/s")
    w("")

    # Phase Results Table
    w("2. PHASE-BY-PHASE RESULTS")
    w("-" * 78)
    header = (
        f"{'Phase':<6} {'Name':<16} {'Users':>8} {'RPS':>8} "
        f"{'p50':>8} {'p95':>8} {'p99':>8} {'Err%':>7} {'OK':>4}"
    )
    w(header)
    w("-" * len(header))

    for p in phases:
        status = "YES" if p.survived and p.error_rate_pct <= MAX_ACCEPTABLE_ERROR_RATE else "NO"
        w(
            f"{p.phase:<6} {p.name:<16} {p.target_users:>8,} {p.rps:>8.1f} "
            f"{p.p50_latency_ms:>7.0f}ms {p.p95_latency_ms:>7.0f}ms "
            f"{p.p99_latency_ms:>7.0f}ms {p.error_rate_pct:>6.1f}% {status:>4}"
        )
    w("")

    # Bottlenecks
    w("3. IDENTIFIED BOTTLENECKS")
    w("-" * 78)
    for i, b in enumerate(analysis.bottlenecks, 1):
        w(f"  {i}. {b}")
    w("")

    # Infrastructure Recommendations
    w("4. INFRASTRUCTURE REQUIREMENTS")
    w("-" * 78)
    for tier_name, spec in analysis.infrastructure.items():
        w(f"  === {tier_name} Users ({spec['concurrent_users']:,} concurrent) ===")
        w(f"    App servers:     {spec['app_servers']} x ({spec['vcpu_per_server']} vCPU, {spec['ram_per_server_gb']}GB RAM)")
        w(f"    Total workers:   {spec['total_workers']}")
        w(f"    Total RAM:       {spec['total_ram_gb']}GB")
        w(f"    DB:              {spec['db_server_type']}")
        w(f"    DB pool size:    {spec['db_pool_size']} connections")
        w(f"    Redis:           {spec['redis_server_type']} ({spec['redis_memory_mb']}MB)")
        w(f"    Load balancer:   {spec['load_balancer']}")
        w(f"    CDN:             {'Yes' if spec['cdn_recommended'] else 'Not required'}")
        w("")

    # Cost Estimates
    w("5. ESTIMATED MONTHLY COSTS (AWS, us-east-1, on-demand)")
    w("-" * 78)
    cost_header = f"{'Tier':<8} {'Compute':>10} {'DB':>8} {'Redis':>8} {'LB':>6} {'CDN':>6} {'Mon':>6} {'TOTAL':>10}"
    w(cost_header)
    w("-" * len(cost_header))
    for tier_name, cost in analysis.cost_estimates.items():
        w(
            f"{tier_name:<8} ${cost['compute']:>9,} ${cost['database']:>7,} "
            f"${cost['redis']:>7,} ${cost['load_balancer']:>5,} "
            f"${cost['cdn']:>5,} ${cost['monitoring']:>5,} ${cost['total_monthly_usd']:>9,}"
        )
    w("")
    w("  NOTE: These are rough estimates using on-demand pricing.")
    w("  Reserved instances or savings plans can reduce costs by 30-60%.")
    w("  Spot instances for non-critical workers can save an additional 50-70%.")
    w("")

    # Recommendations
    w("6. RECOMMENDATIONS")
    w("-" * 78)

    if analysis.recommendations.get("immediate"):
        w("  IMMEDIATE (do now):")
        for r in analysis.recommendations["immediate"]:
            w(f"    * {r}")
        w("")

    if analysis.recommendations.get("short_term"):
        w("  SHORT TERM (next 2-4 weeks):")
        for r in analysis.recommendations["short_term"]:
            w(f"    * {r}")
        w("")

    if analysis.recommendations.get("long_term"):
        w("  LONG TERM (next quarter):")
        for r in analysis.recommendations["long_term"]:
            w(f"    * {r}")
        w("")

    w("=" * 78)
    return "\n".join(lines)


def format_json(analysis: CapacityAnalysis, phases: list[PhaseResult]) -> str:
    """Format the analysis as JSON."""
    return json.dumps({
        "summary": {
            "max_stable_users": analysis.max_stable_users,
            "max_stable_phase": analysis.max_stable_phase,
            "degradation_start_users": analysis.degradation_start_users,
            "breaking_point_users": analysis.breaking_point_users,
            "max_sustained_rps": analysis.max_sustained_rps,
        },
        "phases": [
            {
                "phase": p.phase,
                "name": p.name,
                "users": p.target_users,
                "rps": p.rps,
                "p50_ms": p.p50_latency_ms,
                "p95_ms": p.p95_latency_ms,
                "p99_ms": p.p99_latency_ms,
                "error_rate": p.error_rate_pct,
                "survived": p.survived,
            }
            for p in phases
        ],
        "bottlenecks": analysis.bottlenecks,
        "infrastructure": analysis.infrastructure,
        "cost_estimates": analysis.cost_estimates,
        "recommendations": analysis.recommendations,
    }, indent=2)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Fitsi IA — Capacity Report Analyzer",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    python scripts/capacity_report.py results/stress_test_*/full_report.json
    python scripts/capacity_report.py results/full_report.json --output capacity.md
    python scripts/capacity_report.py results/full_report.json --json
        """,
    )
    parser.add_argument("report_file", help="Path to full_report.json from stress test")
    parser.add_argument("--output", "-o", help="Write report to file (default: stdout)")
    parser.add_argument("--json", action="store_true", help="Output as JSON instead of text")

    args = parser.parse_args()

    report_path = Path(args.report_file)
    if not report_path.exists():
        print(f"ERROR: Report file not found: {report_path}", file=sys.stderr)
        sys.exit(1)

    # Load and analyze
    print(f"Loading results from {report_path}...", file=sys.stderr)
    phases = load_report(str(report_path))
    print(f"Loaded {len(phases)} phases. Analyzing...", file=sys.stderr)

    analysis = analyze(phases)

    # Format output
    if args.json:
        output = format_json(analysis, phases)
    else:
        output = format_report(analysis, phases)

    # Write output
    if args.output:
        output_path = Path(args.output)
        output_path.write_text(output)
        print(f"Report written to {output_path}", file=sys.stderr)
    else:
        print(output)


if __name__ == "__main__":
    main()
